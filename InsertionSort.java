
public class InsertionSort {
  
    public static void main(String[] args) {
     
      int[] arr = {9,4,3,1};
      for(int i=1;i<arr.length;i++){
        int ele = arr[i]; // 4
        int j= i-1; // 0
        while(j>=0 && arr[j]>ele){
          arr[j+1] = arr[j];
          j--;
        }
      arr[j+1] = ele;
      
      }
    }
}


































































































































































